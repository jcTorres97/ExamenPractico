/*validacion, quita la  notificacion que  no se  desactiva*/
$("#Validar-1").change(function () {
    var OrigenI = document.getElementById("Validar").value;

    console.log("entro");

    if (OrigenI == "") {
        $("#Validar-error").css({ "display": "block" });
    }
    else if (OrigenI >= 0) {
        //$('#IdCualOrig').addClass('invisible');
        $("#Validar-error").css({ "display": "none" });
    }
});
